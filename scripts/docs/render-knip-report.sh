#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
OUT_FILE="$ROOT_DIR/docs/system/KNIP_REPORT.md"
TMP_FILE="$(mktemp)"

cleanup() {
  rm -f "$TMP_FILE"
}

trap cleanup EXIT

cd "$ROOT_DIR"

pnpm exec knip \
  --reporter markdown \
  --no-progress \
  --no-config-hints \
  --no-exit-code > "$TMP_FILE"

mkdir -p "$(dirname "$OUT_FILE")"

{
  printf "<!-- Generated by \`pnpm docs:knip\`. Do not edit manually. -->\n\n"
  awk '
    NF {
      blank = 0
      print
      next
    }
    !blank {
      print
      blank = 1
    }
  ' "$TMP_FILE"
} | perl -0pe 's/\n+\z/\n/' > "$OUT_FILE"

echo "Generated: $OUT_FILE"
